<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
    import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				zhaomumingcheng: '',
				zhaomufengmian: '',
				zhaomushijian: '',
				zhaomudidian: '',
				zhaomuyaoqiu: '',
				zhubanfangzhanghao: '',
				zhubanfangmingcheng: '',
				lianxifangshi: '',
				},
				// 登录用户信息
				user: {},
                ro:{
                   zhaomumingcheng : false,
                   zhaomufengmian : false,
                   zhaomushijian : false,
                   zhaomudidian : false,
                   zhaomuyaoqiu : false,
                   zhubanfangzhanghao : false,
                   zhubanfangmingcheng : false,
                   lianxifangshi : false,
                   clicktime : false,
                   clicknum : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.zhubanfangzhanghao = this.user.zhubanfangzhanghao
			this.ro.zhubanfangzhanghao = true;
			this.ruleForm.zhubanfangmingcheng = this.user.zhubanfangmingcheng
			this.ro.zhubanfangmingcheng = true;
			this.ruleForm.lianxifangshi = this.user.lianxifangshi
			this.ro.lianxifangshi = true;



			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`zhiyuanzhezhaomu`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='zhaomumingcheng'){
					this.ruleForm.zhaomumingcheng = obj[o];
					this.ro.zhaomumingcheng = true;
					continue;
					}
					if(o=='zhaomufengmian'){
					this.ruleForm.zhaomufengmian = obj[o].split(",")[0];
					this.ro.zhaomufengmian = true;
					continue;
					}
					if(o=='zhaomushijian'){
					this.ruleForm.zhaomushijian = obj[o];
					this.ro.zhaomushijian = true;
					continue;
					}
					if(o=='zhaomudidian'){
					this.ruleForm.zhaomudidian = obj[o];
					this.ro.zhaomudidian = true;
					continue;
					}
					if(o=='zhaomuyaoqiu'){
					this.ruleForm.zhaomuyaoqiu = obj[o];
					this.ro.zhaomuyaoqiu = true;
					continue;
					}
					if(o=='zhubanfangzhanghao'){
					this.ruleForm.zhubanfangzhanghao = obj[o];
					this.ro.zhubanfangzhanghao = true;
					continue;
					}
					if(o=='zhubanfangmingcheng'){
					this.ruleForm.zhubanfangmingcheng = obj[o];
					this.ro.zhubanfangmingcheng = true;
					continue;
					}
					if(o=='lianxifangshi'){
					this.ruleForm.lianxifangshi = obj[o];
					this.ro.lianxifangshi = true;
					continue;
					}
					if(o=='clicktime'){
					this.ruleForm.clicktime = obj[o];
					this.ro.clicktime = true;
					continue;
					}
					if(o=='clicknum'){
					this.ruleForm.clicknum = obj[o];
					this.ro.clicknum = true;
					continue;
					}
				}
			}
			this.styleChange()
            this.$forceUpdate()
			
			if (uni.getStorageSync('raffleType') && uni.getStorageSync('raffleType') != null) {
				uni.removeStorageSync('raffleType')
				setTimeout(() => {
					this.onSubmitTap()
				}, 300)
			}
		},
		methods: {
            zhaomuyaoqiuChange(e) {
                this.ruleForm.zhaomuyaoqiu = e
            },
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数


			// 日长控件选择日期时间
			zhaomushijianConfirm(val) {
				console.log(val)
				this.ruleForm.zhaomushijian = val.result;
				this.$forceUpdate();
			},
			// 日长控件选择日期时间
			clicktimeConfirm(val) {
				console.log(val)
				this.ruleForm.clicktime = val.result;
				this.$forceUpdate();
			},


			zhaomufengmianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.zhaomufengmian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
//跨表计算判断
				var obj;
				if((!this.ruleForm.zhaomumingcheng)){
					this.$utils.msg(`招募名称不能为空`);
					return
				}
				if((!this.ruleForm.zhaomushijian)){
					this.$utils.msg(`招募时间不能为空`);
					return
				}
				if((!this.ruleForm.zhaomudidian)){
					this.$utils.msg(`招募地点不能为空`);
					return
				}
				if(this.ruleForm.clicknum&&(!this.$validate.isIntNumer(this.ruleForm.clicknum))){
					this.$utils.msg(`点击次数应输入整数`);
					return
				}
				//更新跨表属性
				var crossuserid;
				var crossrefid;
				var crossoptnum;
				if(this.cross){
                    uni.setStorageSync('crossCleanType',true);
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
                        if(!obj) {
						    obj = uni.getStorageSync('crossObj');
                        }
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('appUserid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`zhiyuanzhezhaomu`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
                        uni.removeStorageSync('crossCleanType');
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`zhiyuanzhezhaomu`, this.ruleForm);
						}else{
							await this.$api.add(`zhiyuanzhezhaomu`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`zhiyuanzhezhaomu`, this.ruleForm);
					}else{
						await this.$api.add(`zhiyuanzhezhaomu`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				if(this.ro[str]){
					return false
				}
				this.$refs[str].show();
			}
		}
	}
</script>

